	BTFSS	STATUS,Z
	GOTO	$+3
	MOVLW	DB3
	GOTO	DG014
	XORLW	INTCON^FSR
	BTFSS	STATUS,Z
	GOTO	$+3
	MOVLW	DB4
	GOTO	DG014
	XORLW	PCLATH^INTCON
	BTFSS	STATUS,Z
	GOTO	$+3
	MOVLW	DB5
	GOTO	DG014
	XORLW	DB0^PCLATH
	BTFSC	STATUS,Z
	GOTO	DG012
	BTFSS	FSR,0
	BTFSC	DB1,7
	GOTO	DG013
	XORLW	DB2^DB0
	BTFSS	STATUS,Z
	XORLW	DB3^DB2
	BTFSS	STATUS,Z
	XORLW	DB4^DB3
	BTFSS	STATUS,Z
	XORLW	DB5^DB4
DG012	MOVLW	DB1
	BTFSS	STATUS,Z
DG013	MOVF	DB1,W
	BTFSC	FSR,0
	BSF	STATUS,IRP
DG014	BTFSS	FSR,6
	GOTO	DG015
	MOVWF	FSR
	CALL	DG017
	MOVF	DB1,W
	MOVWF	INDF
	GOTO	DG007
DG015	MOVWF	FSR
	MOVF	INDF,W
DG016	MOVWF	DB1
	GOTO	DG001
